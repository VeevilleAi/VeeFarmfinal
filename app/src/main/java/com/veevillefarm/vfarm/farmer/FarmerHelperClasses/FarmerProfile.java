public class FarmerProfile {

    public String name, farmerPicUrl, mobileNumber, country, email, address;

    public FarmerProfile(String name, String farmerPicUrl, String mobileNumber, String country, String email, String address) {
        this.name = name;
        this.farmerPicUrl = farmerPicUrl;
        this.mobileNumber = mobileNumber;
        this.country = country;
        this.email = email;
        this.address = address;

    }

}
